int	ft_atoi(const char *str)
{
	int	s;
	int	c;
	int	n;

	c = 0;
	s = 1;
	n = 0;
	while (str[c] == 32 || (str[c] >= 9 && str[c] <= 13))
		c++;
	if (str[c] == '-' || str[c] == '+')
	{
		if (str[c] == '-')
			s *= -1;
		c++;
	}
	while (str[c] <= '9' && str[c] >= '0' && str[c] != '\0' )
	{
		n = n * 10 + (str[c] - '0');
		c++;
	}
	return (n * s);
}
